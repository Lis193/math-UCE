#Volumen de una caja
print("Calular el volumen de una caja (ingresar valores en metros)")
print("Ingrese el valor de la altura")
a=float(input())
print("Ingrese el valor de la profundidad")
b=float(input())
print("Ingrese el valor de la longitud")
c=float(input())
v=a*b*c
print("El volumen de la caja es ",v," metros")
